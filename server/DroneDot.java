public class DroneDot {
    private int x;
    private int y;
    private int Id;

    public DroneDot(int x, int y, int Id) {
        this.x = x;
        this.y = y;
        this.Id = Id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getID() {
        return Id;
    }

}
